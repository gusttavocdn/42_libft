#include "libft.h"

int	ft_isalnum(int c)
{
	if ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z'))
		return (TRUE);
	if (c >= '0' && c <= '9')
		return (TRUE);
	return (FALSE);
}
